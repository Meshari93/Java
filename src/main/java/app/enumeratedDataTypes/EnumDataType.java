package app.enumeratedDataTypes;
// A simple enum example where enum is declared outside any class (Note enum keyword instead of
// class keyword)

public class EnumDataType {

  public void enumDataType() {
    Color red = Color.RED;
    Color green = Color.GREEN;
    Color blue = Color.BLUE;
    System.out.println("Enum Red: " + red);
    System.out.println("Enum Green: " + green);
    System.out.println("Enum Blue: " + blue);
  }
}
